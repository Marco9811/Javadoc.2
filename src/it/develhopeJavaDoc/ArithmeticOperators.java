public class ArithmeticOperators {
    static int num1 = 10;
    static int num2 = 5;
    /**
     * Metodo richiesto per stampare la SOMMA di num1, num2. 10+5=15.
     */
    static void sum() {
        System.out.println(num1 + num2);
    }
    /**
     * Metodo richiesto per stampare la SOTTRAZIONE di num1, num2. 10-5=5.
     */
    static void sub() {
        System.out.println(num1 - num2);
    }
    /**
     * Metodo richiesto per stampare la MOLTIPLICAZIONE di num1, num2. 10*5=50.
     */
    static void mul() {
        System.out.println(num1 * num2);
    }
    /**
     * Metodo richiesto per stampare la DIVISIONE di num1, num2. 10/5=2.
     */
    static void div() {
        System.out.println(num1 / num2);
    }
}





